"""Function to carry out basic mathematical operations"""


def addition(int1: int, int2: int) -> int:
    """A function to replicate addition"""
    return int1 + int2


def subtraction(num_1: int, num_2: int) -> int:
    """A function to replicate subtraction"""
    return num_1 - num_2


def multiplication(num_1: int, num_2: int) -> int:
    """A function to replace multiplication"""
    return num_1 * num_2


def division(num_1: int, num_2: int) -> int:
    """A function to replace division"""
    return num_1 / num_2

